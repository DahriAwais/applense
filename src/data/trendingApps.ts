export function computeAppHomeStats(app: any) {
  // Safe downloads parsing (iOS uses multiplier whereas Android uses exact installs)
  let totalDownloads = 100000;
  const rawTotal = app.derivedDownloads || app.estimatedDownloads || app.installsCount || 100000;
  
  if (typeof rawTotal === "number") {
    totalDownloads = rawTotal;
  } else {
    const str = String(rawTotal).toLowerCase().replace(/,/g, "").trim();
    if (str.includes("b")) {
      totalDownloads = (parseFloat(str.replace(/[^0-9.]/g, "")) || 1) * 1000000000;
    } else if (str.includes("m")) {
      totalDownloads = (parseFloat(str.replace(/[^0-9.]/g, "")) || 1) * 1000000;
    } else if (str.includes("k")) {
      totalDownloads = (parseFloat(str.replace(/[^0-9.]/g, "")) || 1) * 1000;
    } else {
      const clean = str.replace(/[^0-9]/g, "");
      totalDownloads = parseInt(clean, 10) || 100000;
    }
  }

  // IOS logic vs Android logic for total downloads
  if (app.store === "apple" && !app.derivedDownloads) {
    // IOS uses Reviews Multiplier formula!
    const genreLower = (app.genre || "").toLowerCase();
    const isGamingOrEntertainment = 
      genreLower.includes("game") || 
      genreLower.includes("gaming") || 
      genreLower.includes("entertainment") || 
      genreLower.includes("play");
    const multiplier = isGamingOrEntertainment ? 45 : 70;
    const ratingsCount = app.reviewsCount || app.userRatingCount || 100;
    totalDownloads = Math.max(100, ratingsCount * multiplier);
  }

  // Calculate age of the app to divide totalDownloads correctly
  let ageMonths = 36;
  try {
    const released = app.released;
    if (released && released !== "N/A" && released !== "varies") {
      const releasedStr = String(released).replace(/\\/g, "");
      const releasedDate = new Date(releasedStr);
      if (!isNaN(releasedDate.getTime())) {
        const currentDate = new Date("2026-06-01");
        const diffYears = currentDate.getFullYear() - releasedDate.getFullYear();
        const diffMonths = currentDate.getMonth() - releasedDate.getMonth();
        const totalMonths = (diffYears * 12) + diffMonths;
        ageMonths = Math.max(6, totalMonths);
      }
    }
  } catch {
    ageMonths = 36;
  }

  const monthlyDownloads = Math.max(1, Math.round(totalDownloads / ageMonths));
  const price = app.price || 0;
  let revenue = 0;

  // Decide model: If defaultModel is specified, use it. Otherwise, auto-detect Freemium.
  let currentModel: "freemium" | "paid" | "ads" = "ads";
  if (app.defaultModel) {
    currentModel = app.defaultModel;
  } else if (price > 0) {
    currentModel = "paid";
  } else {
    // Famous freemium app detection or general SaaS genre detection:
    const titleLower = (app.title || "").toLowerCase();
    const isFamousFreemium = 
      titleLower.includes("spotify") ||
      titleLower.includes("chatgpt") ||
      titleLower.includes("canva") ||
      titleLower.includes("discord") ||
      titleLower.includes("zoom") ||
      titleLower.includes("slack") ||
      titleLower.includes("notion") ||
      titleLower.includes("capcut") ||
      titleLower.includes("headspace") ||
      titleLower.includes("calm") ||
      titleLower.includes("duolingo") ||
      titleLower.includes("duingo") ||
      titleLower.includes("duet display") ||
      titleLower.includes("lightroom");
      
    if (isFamousFreemium) {
      currentModel = "freemium";
    } else {
      currentModel = "ads";
    }
  }

  if (currentModel === "freemium") {
    // Exact same formula as in App.tsx: (Monthly Downloads * Conversion Rate of 2.0% * Subscription price of 9.99) / 100
    revenue = (monthlyDownloads * 2.0 * 9.99) / 100;
  } else if (currentModel === "paid") {
    revenue = monthlyDownloads * price;
  } else {
    // Ad-Supported Model: (Monthly Downloads * Active User Ratio * Daily Impressions * Average eCPM) / 1,000
    // Standard default sliders: Active Ratio = 30%, Impressions = 3, eCPM = $2.50
    revenue = (monthlyDownloads * 30 * 3 * 2.50) / 1000;
  }

  return {
    monthlyDownloads: monthlyDownloads,
    monthlyRevenue: Math.round(revenue),
    totalDownloads: totalDownloads
  };
}
